

# Vamos a probar la función print (recordá que esta línea es un comentario)
print("Hola, Python")



# Ejemplo con \n
print("Hola mundo!\n Hola Python\n")

## Salida consola
# Hola mundo!
# Hola Python


# Múltiples argumentos
# Puedes pasar varios argumentos separados por comas:

print("Hola", "Python")
# Salida consola
# Hola Python


# Argumento end
print("Hola", end="-")
print("Mundo")

# Salida consola
# Hola-Mundo

# Argumento sep
print("Python", "es", "genial", sep="-")

# Salida consola
# Python-es-genial


# Combinando sep y end
print("Aprender", "Python", sep="->", end="!!!\n")

# Salida consola
# Aprender->Python!!!



