

import math # es un modulo nativo de python


# vamos a ver algunas funciones que nos ofrece el modulo math

# ceil() -> nos va a devolver el entero más pequeño mayor o igual que un número
print(math.ceil(3.4)) # salida: 4

# floor() -> nos va a devolver el entero más grande menor o igual que un número
print(math.floor(3.4)) # salida: 3

# pow() -> nos va a devolver la potencia de un número
print(math.pow(2,3)) # salida: 8.0

# sqrt() -> nos va a devolver la raíz cuadrada de un número
print(math.sqrt(25)) # salida: 5.0


# Luego vemos dos funciones que forman parte directamente de Python sobre matemáticas

# abs() -> nos va a devolver el valor absoluto de un número

print(abs(-5)) # salida: 5

#round() -> nos va a devolver el valor redondeado de un número

print(round(3.56777, 2)) # salida: 3.57






